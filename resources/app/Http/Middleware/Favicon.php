            $response = new Response();

            $response = $response->withHeader('Content-Type', 'file/x-icon');
            $response = $response->withHeader(
                'Content-Length',
                \mb_strlen(\base64_decode(self::FAVICON_DATA, true)),
            );

            $response->getBody()->write(\base64_decode(self::FAVICON_DATA, true));

            return $response;
        }

        return $handler->handle($request);
